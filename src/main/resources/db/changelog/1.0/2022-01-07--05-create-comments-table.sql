CREATE TABLE COMMENTS(ID BIGINT AUTO_INCREMENT PRIMARY KEY,
MESSAGE VARCHAR(250),
DATE_MESSAGE TIMESTAMP,
ID_USER BIGINT,
ID_POST BIGINT,
FOREIGN KEY (ID_USER) REFERENCES USERS(ID),
FOREIGN KEY (ID_POST) REFERENCES POSTS(ID) ON DELETE CASCADE);